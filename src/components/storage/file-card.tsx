/**
 * FileCard Component
 *
 * Displays a file in the file manager with preview and actions.
 * Renders actual images for IMAGE category, inline video player for VIDEO,
 * and file type icons for everything else.
 *
 * VIDEO PLAYBACK:
 * - Click play button to start inline video playback
 * - Videos start muted (browser requirement)
 * - PUBLIC videos use CDN HLS URL
 * - PRIVATE videos use authenticated API route
 *
 * @example
 * <FileCard
 *   id="file_123"
 *   organizationId="org_456"
 *   name="course-video.mp4"
 *   extension="mp4"
 *   category="VIDEO"
 *   size={1024000}
 *   visibility="PUBLIC"
 *   hlsManifestUrl="https://cdn.../master.m3u8"
 *   posterUrl="https://cdn.../poster.jpg"
 *   videoProcessingStatus="COMPLETED"
 *   onClick={() => selectFile('file_123')}
 * />
 */
export function FileCard({
  id,
  organizationId,
  name,
  extension,
  category,
  size,
  thumbnailUrl,
  accessUrl,
  publicUrl,
  visibility,
  hlsManifestUrl,
  posterUrl,
  videoProcessingStatus,
  isSelected,
  onClick,
  onDoubleClick,
  onRename,
  isRenaming,
  onDelete,
  onMove,
  onDownload,
  onToggleVisibility,
  className,
}: FileCardProps) {
  // State for inline video playback
  const [isVideoPlaying, setIsVideoPlaying] = useState(false)

  // State for inline name editing
  const [isEditing, setIsEditing] = useState(false)
  const [editName, setEditName] = useState(name)
  const inputRef = useRef<HTMLInputElement>(null)

  const isImage = category === 'IMAGE'
  const isVideo = category === 'VIDEO'

  // Both PUBLIC and PRIVATE files can have shareable links
  // PUBLIC: Direct CDN URL (anyone can access)
  // PRIVATE: Authenticated API route (only logged-in org members can access)
  const shareableUrl = visibility === 'PUBLIC' ? publicUrl : `/api/storage/${id}`
  const canCopyLink = !!shareableUrl

  // Handlers for video playback
  const handlePlayVideo = useCallback(() => {
    setIsVideoPlaying(true)
  }, [])

  const handleCloseVideo = useCallback(() => {
    setIsVideoPlaying(false)
  }, [])

  // Focus input when entering edit mode
  useEffect(() => {
    if (isEditing && inputRef.current) {
      inputRef.current.focus()
      inputRef.current.select()
    }
  }, [isEditing])

  // Reset edit name when name prop changes (e.g., after successful rename)
  useEffect(() => {
    setEditName(name)
  }, [name])

  /**
   * Enter edit mode on double-click
   */
  const handleNameDoubleClick = useCallback((e: React.MouseEvent) => {
    e.stopPropagation()
    if (!onRename) return
    setIsEditing(true)
  }, [onRename])

  /**
   * Save the new name
   */
  const handleSaveName = useCallback(() => {
    const trimmedName = editName.trim()
    if (trimmedName && trimmedName !== name) {
      onRename?.(trimmedName)
    }
    setIsEditing(false)
  }, [editName, name, onRename])

  /**
   * Cancel editing and reset name
   */
  const handleCancelEdit = useCallback(() => {
    setEditName(name)
    setIsEditing(false)
  }, [name])

  /**
   * Handle keyboard events in edit mode
   */
  const handleKeyDown = useCallback((e: React.KeyboardEvent) => {
    if (e.key === 'Enter') {
      e.preventDefault()
      handleSaveName()
    } else if (e.key === 'Escape') {
      e.preventDefault()
      handleCancelEdit()
    }
  }, [handleSaveName, handleCancelEdit])

  return (
    <div
      className={cn(
        'group relative flex flex-col rounded-xl overflow-hidden cursor-pointer transition-all duration-200',
        'border border-border/50 bg-card',
        'hover:border-border hover:shadow-md',
        isSelected && 'ring-2 ring-primary border-primary',
        className
      )}
      onClick={onClick}
      onDoubleClick={onDoubleClick}
    >
      {/* Preview/Icon area */}
      <div className="relative aspect-square bg-muted/30 flex items-center justify-center overflow-hidden">
        {isImage ? (
          // Render image preview (public shows image, private shows lock icon)
          <ImagePreview
            accessUrl={accessUrl}
            thumbnailUrl={thumbnailUrl}
            name={name}
            isPrivate={visibility === 'PRIVATE'}
          />
        ) : isVideo ? (
          // Render video preview with inline player support
          // Supports both PUBLIC (CDN) and PRIVATE (API route) videos
          <VideoPreview
            fileId={id}
            organizationId={organizationId}
            thumbnailUrl={thumbnailUrl}
            posterUrl={posterUrl}
            hlsManifestUrl={hlsManifestUrl}
            visibility={visibility}
            videoProcessingStatus={videoProcessingStatus}
            name={name}
            isPlaying={isVideoPlaying}
            onPlayClick={handlePlayVideo}
            onCloseClick={handleCloseVideo}
          />
        ) : (
          // File type icon for non-visual files
          <FileIcon extension={extension} category={category} size="lg" />
        )}

        {/* Visibility badge - hide when video is playing */}
        {!isVideoPlaying && (
          <Badge
            variant={visibility === 'PUBLIC' ? 'default' : 'secondary'}
            className={cn(
              'absolute top-2 left-2 text-[10px] px-1.5 py-0.5',
              visibility === 'PUBLIC'
                ? 'bg-green-500/90 hover:bg-green-500'
                : 'bg-muted/90'
            )}
          >
            {visibility === 'PUBLIC' ? (
              <Globe className="w-3 h-3 mr-1" />
            ) : (
              <Lock className="w-3 h-3 mr-1" />
            )}
            {visibility === 'PUBLIC' ? 'Public' : 'Private'}
          </Badge>
        )}

        {/* Context menu trigger - hide when video is playing */}
        {!isVideoPlaying && (
          <DropdownMenu modal={true}>
            <DropdownMenuTrigger asChild>
              <Button
                variant="ghost"
                size="icon"
                className={cn(
                  'absolute top-2 right-2 h-7 w-7',
                  'bg-background/80 backdrop-blur-sm',
                  'opacity-0 group-hover:opacity-100 transition-opacity',
                  'hover:bg-background'
                )}
                onClick={(e) => e.stopPropagation()}
              >
                <MoreHorizontal className="h-4 w-4" />
              </Button>
            </DropdownMenuTrigger>
          <DropdownMenuContent
            align="end"
            className="w-48"
            onClick={(e) => e.stopPropagation()}
          >
            <DropdownMenuItem
              onClick={(e) => {
                e.stopPropagation()
                onDownload?.()
              }}
            >
              <Download className="w-4 h-4 mr-2" />
              Download
            </DropdownMenuItem>
            <DropdownMenuItem
              onClick={(e) => {
                e.stopPropagation()
                if (shareableUrl) {
                  // For private files, build full URL with origin
                  const fullUrl = visibility === 'PRIVATE'
                    ? `${window.location.origin}${shareableUrl}`
                    : shareableUrl
                  // Show appropriate message based on visibility
                  const message = visibility === 'PRIVATE'
                    ? 'Link copied - accessible for 15 minutes after opening'
                    : 'Link copied to clipboard'
                  copyToClipboard(fullUrl, message)
                } else {
                  toast.error('No URL available')
                }
              }}
            >
              <Link2 className="w-4 h-4 mr-2" />
              Copy Link
              {visibility === 'PRIVATE' && <Lock className="w-3 h-3 ml-auto text-muted-foreground" />}
            </DropdownMenuItem>
            <DropdownMenuItem
              onClick={(e) => {
                e.stopPropagation()
                // Enter inline edit mode - same as double-click on file name
                setIsEditing(true)
              }}
            >
              <Pencil className="w-4 h-4 mr-2" />
              Rename
            </DropdownMenuItem>
            <DropdownMenuItem
              onClick={(e) => {
                e.stopPropagation()
                onMove?.()
              }}
            >
              <Move className="w-4 h-4 mr-2" />
              Move to...
            </DropdownMenuItem>
            <DropdownMenuSeparator />
            <DropdownMenuItem
              onClick={(e) => {
                e.stopPropagation()
                onToggleVisibility?.()
              }}
            >
              {visibility === 'PUBLIC' ? (
                <>
                  <Lock className="w-4 h-4 mr-2" />
                  Make Private
                </>
              ) : (
                <>
                  <Globe className="w-4 h-4 mr-2" />
                  Make Public
                </>
              )}
            </DropdownMenuItem>
            <DropdownMenuSeparator />
            <DropdownMenuItem
              onClick={(e) => {
                e.stopPropagation()
                onDelete?.()
              }}
              className="text-destructive focus:text-destructive"
            >
              <Trash2 className="w-4 h-4 mr-2" />
              Delete
            </DropdownMenuItem>
          </DropdownMenuContent>
        </DropdownMenu>
        )}
      </div>

      {/* File info with inline rename */}
      <div className="p-3 space-y-1">
        {isEditing ? (
          // Inline edit mode
          <div className="flex items-center gap-1">
            <input
              ref={inputRef}
              type="text"
              value={editName}
              onChange={(e) => setEditName(e.target.value)}
              onKeyDown={handleKeyDown}
              onBlur={handleSaveName}
              onClick={(e) => e.stopPropagation()}
              disabled={isRenaming}
              className={cn(
                'flex-1 min-w-0 text-sm font-medium bg-transparent',
                'border-b border-primary outline-none',
                'focus:border-primary',
                isRenaming && 'opacity-50'
              )}
            />
          </div>
        ) : (
          // Display mode - double-click to edit
          <p
            className={cn(
              'text-sm font-medium truncate',
              onRename && 'cursor-text hover:bg-muted/50 rounded px-1 -mx-1 transition-colors'
            )}
            title={`${name} (double-click to rename)`}
            onDoubleClick={handleNameDoubleClick}
          >
            {name}
          </p>
        )}
        <p className="text-xs text-muted-foreground">
          {formatFileSize(size)} • {extension.toUpperCase()}
        </p>
      </div>
    </div>
  )
}

