/**
 * Image Preview Component
 *
 * Renders actual image preview for both PUBLIC and PRIVATE files:
 * - PUBLIC files: Uses next/image with direct CDN URL (fast, cacheable)
 * - PRIVATE files: Uses regular <img> tag with accessUrl (/api/storage/[fileId])
 *   which redirects to a presigned R2 URL. next/image can't follow redirects,
 *   but a regular <img> tag handles them natively. Same pattern as VideoPreview
 *   for private video posters.
 */
function ImagePreview({
  accessUrl,
  thumbnailUrl,
  name,
  isPrivate,
}: {
  accessUrl?: string | null
  thumbnailUrl?: string | null
  name: string
  isPrivate?: boolean
}) {
  const imageUrl = accessUrl || thumbnailUrl

  // No URL available — show placeholder
  if (!imageUrl) {
    return (
      <div className="flex flex-col items-center justify-center gap-2 text-muted-foreground h-full">
        {isPrivate ? (
          <>
            <Lock className="w-8 h-8 opacity-50" />
            <span className="text-xs">Private</span>
          </>
        ) : (
          <>
            <Globe className="w-8 h-8 opacity-50" />
            <span className="text-xs">No Preview</span>
          </>
        )}
      </div>
    )
  }

  /**
   * Private files: Use regular <img> tag because the accessUrl is an API route
   * (/api/storage/[fileId]) that returns a 302 redirect to a presigned R2 URL.
   * next/image can't handle redirects, but <img> follows them natively.
   */
  if (isPrivate) {
    return (
      <img
        src={imageUrl}
        alt={name}
        className="absolute inset-0 w-full h-full object-cover"
      />
    )
  }

  // Public files: Use next/image for optimized loading via CDN
  return (
    <Image
      src={imageUrl}
      alt={name}
      fill
      className="object-cover"
      sizes="(max-width: 640px) 50vw, (max-width: 1024px) 25vw, 16vw"
    />
  )
}

/**
 * Video Preview Component with Inline Playback
 *
 * Shows thumbnail with play button overlay for videos.
 * When play button is clicked, shows inline video player.
 * Supports both PUBLIC (CDN URL) and PRIVATE (API route) videos.
 *
 * PUBLIC videos: Use hlsManifestUrl (CDN) directly
 * PRIVATE videos: Use fileId + organizationId (API route with auth)
 */
function VideoPreview({
  fileId,
  organizationId,
  thumbnailUrl,
  posterUrl,
  hlsManifestUrl,
  visibility,
  videoProcessingStatus,
  name,
  isPlaying,
  onPlayClick,
  onCloseClick,
}: {
  fileId: string
  organizationId?: string
  thumbnailUrl?: string | null
  posterUrl?: string | null
  hlsManifestUrl?: string | null
  visibility: FileVisibility
  videoProcessingStatus?: VideoProcessingStatus | null
  name: string
  isPlaying: boolean
  onPlayClick: () => void
  onCloseClick: () => void
}) {
  // Determine if video is ready to play (processing complete)
  const isVideoReady = videoProcessingStatus === 'COMPLETED'

  // Construct poster URL based on visibility
  // For PRIVATE videos, poster might need to go through API route
  // For PUBLIC videos, use the CDN URL directly
  const getEffectivePosterUrl = (): { url: string; isApiRoute: boolean } | null => {
    // If we have a direct poster URL (PUBLIC videos), use it
    if (posterUrl) return { url: posterUrl, isApiRoute: false }
    // If we have a thumbnail, use it
    if (thumbnailUrl) return { url: thumbnailUrl, isApiRoute: false }
    // For completed videos, construct poster URL via API route
    // This handles PRIVATE videos where posterUrl is null but posterKey exists
    if (isVideoReady) {
      return { url: `/api/storage/hls/${fileId}/poster.jpg`, isApiRoute: true }
    }
    return null
  }

  const posterData = getEffectivePosterUrl()
  const previewImage = posterData?.url || null
  const isApiRoutePoster = posterData?.isApiRoute || false
  const hasPreview = !!previewImage

  // If video is playing, show the video player
  if (isPlaying && isVideoReady) {
    return (
      <div className="absolute inset-0 bg-black z-20">
        {/* Close button to return to thumbnail */}
        <Button
          variant="ghost"
          size="icon"
          className="absolute top-2 right-2 z-30 h-7 w-7 bg-black/70 hover:bg-black/90 text-white"
          onClick={(e) => {
            e.stopPropagation()
            onCloseClick()
          }}
        >
          <X className="w-4 h-4" />
        </Button>

        {/* Inline Video Player - fill the card container */}
        {visibility === 'PUBLIC' && hlsManifestUrl ? (
          // PUBLIC: Use CDN URL directly
          <VideoPlayer
            src={hlsManifestUrl}
            poster={posterUrl || thumbnailUrl || undefined}
            autoPlay
            muted
            controls
            className="!aspect-auto absolute inset-0 !rounded-none"
          />
        ) : (
          // PRIVATE: Use fileId + organizationId (authenticated API route)
          <VideoPlayer
            fileId={fileId}
            organizationId={organizationId}
            autoPlay
            muted
            controls
            className="!aspect-auto absolute inset-0 !rounded-none"
          />
        )}
      </div>
    )
  }

  // Show thumbnail with play button overlay
  return (
    <div className="relative w-full h-full">
      {hasPreview ? (
        <>
          {/* Video thumbnail/poster */}
          {/* Use regular img for API routes (Next.js Image doesn't handle them well) */}
          {isApiRoutePoster ? (
            <img
              src={previewImage!}
              alt={name}
              className="absolute inset-0 w-full h-full object-cover"
            />
          ) : (
            <Image
              src={previewImage!}
              alt={name}
              fill
              className="object-cover"
              sizes="(max-width: 640px) 50vw, (max-width: 1024px) 25vw, 16vw"
            />
          )}
          {/* Play button overlay */}
          {isVideoReady ? (
            <div
              className="absolute inset-0 flex items-center justify-center bg-black/20 hover:bg-black/30 transition-colors cursor-pointer"
              onClick={(e) => {
                e.stopPropagation()
                onPlayClick()
              }}
            >
              <div className="w-12 h-12 rounded-full bg-black/60 hover:bg-black/80 flex items-center justify-center transition-colors">
                <Play className="w-6 h-6 text-white fill-white ml-1" />
              </div>
            </div>
          ) : (
            // Video not ready - show processing indicator
            <div className="absolute inset-0 flex items-center justify-center bg-black/40">
              <div className="text-center">
                <div className="w-10 h-10 rounded-full bg-black/60 flex items-center justify-center mx-auto mb-1">
                  <Play className="w-5 h-5 text-white/50 ml-0.5" />
                </div>
                <span className="text-[10px] text-white/80">
                  {videoProcessingStatus === 'PROCESSING' ? 'Processing...' :
                   videoProcessingStatus === 'PENDING' ? 'Queued' :
                   videoProcessingStatus === 'FAILED' ? 'Failed' : 'Not ready'}
                </span>
              </div>
            </div>
          )}
        </>
      ) : (
        // No thumbnail available - show video placeholder with play icon
        <div className="flex flex-col items-center justify-center gap-2 text-muted-foreground bg-muted/50 h-full">
          <div
            className={cn(
              "w-14 h-14 rounded-full bg-muted flex items-center justify-center",
              isVideoReady && "cursor-pointer hover:bg-muted/80 transition-colors"
            )}
            onClick={isVideoReady ? (e) => {
              e.stopPropagation()
              onPlayClick()
            } : undefined}
          >
            <Play className="w-7 h-7 ml-1" />
          </div>
          <span className="text-xs">
            {videoProcessingStatus === 'PROCESSING' ? 'Processing...' :
             videoProcessingStatus === 'PENDING' ? 'Queued' :
             videoProcessingStatus === 'FAILED' ? 'Failed' :
             isVideoReady ? 'Video' : 'Video'}
          </span>
        </div>
      )}
    </div>
  )
}

