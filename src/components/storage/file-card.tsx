/**
 * Copy text to clipboard
 * Uses modern Clipboard API which requires user gesture context
 * @param text - The text to copy
 * @param successMessage - Custom success message to show
 */
function copyToClipboard(text: string, successMessage: string = 'Link copied to clipboard'): void {
  // The Clipboard API needs to be called synchronously within user gesture
  // Using setTimeout to ensure dropdown doesn't interfere
  setTimeout(() => {
    // Create a temporary input that's visible (required for some browsers)
    const input = document.createElement('input')
    input.setAttribute('value', text)
    input.style.position = 'fixed'
    input.style.left = '0'
    input.style.top = '0'
    input.style.opacity = '0'
    input.style.pointerEvents = 'none'
    document.body.appendChild(input)
    input.select()
    input.setSelectionRange(0, text.length) // For mobile

    try {
      const successful = document.execCommand('copy')
      if (successful) {
        toast.success(successMessage)
      } else {
        // Try Clipboard API as backup
        navigator.clipboard?.writeText(text)
          .then(() => toast.success(successMessage))
          .catch(() => toast.error('Failed to copy link'))
      }
    } catch {
      toast.error('Failed to copy link')
    }

    document.body.removeChild(input)
  }, 100)
}

/**
 * Formats file size to human-readable string
 * e.g., 1024 -> "1 KB", 1048576 -> "1 MB"
 */
function formatFileSize(bytes: number): string {
  if (bytes === 0) return '0 B'

  const units = ['B', 'KB', 'MB', 'GB', 'TB']
  const k = 1024
  const i = Math.floor(Math.log(bytes) / Math.log(k))

  return `${parseFloat((bytes / Math.pow(k, i)).toFixed(1))} ${units[i]}`
}

/**
 * Check if file category can have a visual preview
 */
function isVisualFile(category: FileCategory): boolean {
  return category === 'IMAGE' || category === 'VIDEO'
}

